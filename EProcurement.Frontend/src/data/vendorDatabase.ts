// Helper functions
export const getVendorsBySubClassification = (subClassificationCode: string): VendorRecord[] => {
  return vendorDatabase.filter(
    vendor => vendor.subClassificationCode === subClassificationCode && vendor.isActive
  );
};

export const getVendorsByMultipleSubClassifications = (subClassificationCodes: string[]): VendorRecord[] => {
  const uniqueVendors = new Map<string, VendorRecord>();
  
  subClassificationCodes.forEach(code => {
    const vendors = getVendorsBySubClassification(code);
    vendors.forEach(vendor => {
      // Use vendorName as unique key to avoid duplicates
      if (!uniqueVendors.has(vendor.vendorName)) {
        uniqueVendors.set(vendor.vendorName, vendor);
      }
    });
  });
  
  return Array.from(uniqueVendors.values()).sort((a, b) => 
    (b.rating || 0) - (a.rating || 0) // Sort by rating descending
  );
};

export const searchVendors = (searchTerm: string): VendorRecord[] => {
  const term = searchTerm.toLowerCase();
  return vendorDatabase.filter(vendor => 
    vendor.isActive && (
      vendor.vendorName.toLowerCase().includes(term) ||
      vendor.category.toLowerCase().includes(term) ||
      vendor.classification.toLowerCase().includes(term) ||
      vendor.subClassification.toLowerCase().includes(term) ||
      vendor.vendorCode?.toLowerCase().includes(term) ||
      vendor.contactPerson?.toLowerCase().includes(term)
    )
  );
};

export const addVendorRecord = (vendor: Omit<VendorRecord, 'id' | 'createdDate' | 'updatedDate'>): VendorRecord => {
  const newVendor: VendorRecord = {
    ...vendor,
    id: `VDB-${(vendorDatabase.length + 1).toString().padStart(3, '0')}`,
    createdDate: new Date().toISOString().split('T')[0],
    updatedDate: new Date().toISOString().split('T')[0],
  };
  vendorDatabase.push(newVendor);
  return newVendor;
};

export const updateVendorRecord = (id: string, updates: Partial<VendorRecord>): VendorRecord | null => {
  const index = vendorDatabase.findIndex(v => v.id === id);
  if (index === -1) return null;
  
  vendorDatabase[index] = {
    ...vendorDatabase[index],
    ...updates,
    updatedDate: new Date().toISOString().split('T')[0],
  };
  
  return vendorDatabase[index];
};

export const deleteVendorRecord = (id: string): boolean => {
  const index = vendorDatabase.findIndex(v => v.id === id);
  if (index === -1) return false;
  
  // Soft delete by setting isActive to false
  vendorDatabase[index].isActive = false;
  vendorDatabase[index].updatedDate = new Date().toISOString().split('T')[0];
  
  return true;
};

export const getAllVendors = (): VendorRecord[] => {
  return vendorDatabase.filter(v => v.isActive);
};

export const getAllActiveVendors = (): VendorRecord[] => {
  return vendorDatabase.filter(v => v.isActive);
};

export const getVendorsBySubClassificationName = (subClassificationName: string): VendorRecord[] => {
  return vendorDatabase.filter(
    vendor => vendor.subClassification.toLowerCase() === subClassificationName.toLowerCase() && vendor.isActive
  );
};

export const getVendorById = (id: string): VendorRecord | undefined => {
  return vendorDatabase.find(v => v.id === id && v.isActive);
};
